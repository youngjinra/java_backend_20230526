public class Main {

    // 주석문(메모용도,실행시키고 싶지 않을때 사용,코멘트 달기,지우기 부담스러울 때)
    // 자바 코드의 시작은 main 메서드이다.
    public static void main(String[] args) {
        System.out.println("안녕, 세상!");
        System.out.println("10" + "10"); // 숫자가 아님(산술연산 불가능)
        System.out.println(10 + 10); // 숫자(산술연산 가능)

        // 문자, 숫자, 논리 등등 -> 타입(형)

        //출력문을 문자로 아무거나 5개, 숫자로 아무거나 5개 출력

        System.out.println("홍길동");
        System.out.println("호날두");
        System.out.println("김강찬");
        System.out.println("다비드");
        System.out.println("메시");
        System.out.println(1);
        System.out.println(1023);
        System.out.println(2030);
        System.out.println(620304);
        System.out.println(2034444445);

    }
}
